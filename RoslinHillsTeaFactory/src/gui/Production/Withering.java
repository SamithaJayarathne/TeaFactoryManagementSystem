    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed

        String batch = jTextField1.getText();
        String machine = jTextField2.getText();
        String humidity = jLabel10.getText();
        String temperature = jLabel1.getText();
        String startTime = jTextField3.getText();
        String endTime = jTextField4.getText();

        try {

            if (batch.isEmpty()) {
                JOptionPane.showMessageDialog(this, "Select Batch", "Warning", JOptionPane.WARNING_MESSAGE);
            } else if (machine.isEmpty()) {
                JOptionPane.showMessageDialog(this, "Select Machine", "Warning", JOptionPane.WARNING_MESSAGE);
            } else if (humidity == "00") {
                JOptionPane.showMessageDialog(this, "Set Humidity", "Warning", JOptionPane.WARNING_MESSAGE);
            } else if (temperature == "00") {
                JOptionPane.showMessageDialog(this, "Set Temperature", "Warning", JOptionPane.WARNING_MESSAGE);
            } else if (startTime.isEmpty()) {
                JOptionPane.showMessageDialog(this, "Set Temperature", "Warning", JOptionPane.WARNING_MESSAGE);
            } else if (endTime.isEmpty()) {
                JOptionPane.showMessageDialog(this, "Set Temperature", "Warning", JOptionPane.WARNING_MESSAGE);
            } else {

                MySQL.executeIUD("INSERT INTO `withering` (`tea_batch_id`, `start_time`, `end_time`, `humidity_level`, `temperature_level`, `production_status_id`)"
                        + "VALUES ('" + batch + "','" + startTime + "','" + endTime + "','" + humidity + "','" + temperature + "','1')");

            }

        } catch (Exception e) {
            e.printStackTrace();
        }

    }//GEN-LAST:event_jButton3ActionPerformed

