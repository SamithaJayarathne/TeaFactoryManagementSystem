    private void supp() {
    try {
        ResultSet count =model.MySQL.executeSearch("SELECT COUNT(*) AS total FROM suppliers");
        if (count.next()) {
            int totalCount = count.getInt("total");  
            jLabel4.setText(String.valueOf(totalCount));  
        }else{
            jLabel4.setText(String.valueOf("0")); 
        }
    } catch (Exception e) {
        e.printStackTrace();
    }
}


